                <h4>Receitas exclusivas</h4>
                <p class="txt1"><span>BACALHAU CONFITADO EM<br>
                        AZEITE EXTRA VIRGEM BATALHA<br>
                        COM PURÊ DE GRÃO DE BICO<br>
                        4 PORÇÕES</span></p>
                <p class="text-uppercase txt2">
                    <span>Ingredientes:</span><br>
                    4 filés altos de bacalhau<br>
                    (gadus Morhua) com 150 gr cada<br>
                    4 tomates maduros<br>
                    2 dentes de alho cortados em lâminas<br>
                    300 ml de azeite Batalha Extra Virgem<br>
                    500 gr de grão de bico cozido<br>
                    300 ml da água do cozimento dos grãos de bico<br>
                    Sal a gosto
                </p>
                <p class="txt3">
                    <span class="text-uppercase">Modo de preparo:</span><br>
                    Dessalgue o bacalhau com pelo menos 24 horas de antecedência.<br>
                    Coloque os filés de baca;hau uma forma de vidro e reserve.<br>
                    Corte os tomates ao meio, retire as sementes e coloque o alho picado.<br>
                    Coloque os tomates junto ao bacalhau na forma e cubra com o<br>
                    Azeite Batalha Extra Virgem, levando ao forno a 150ºC por 2 horas.<br>
                    Para o purê, processe o grão de bico com a água do seu<br>
                    cozimento e reserve. Quando o bacalhau estiver pronto, aqueça<br>
                    o purê em fogo baixo e sirva com o bacalhau,<br>
                    finalizando com um fio de azeite Batalha Extra Virgem.
                </p>
